    private void txtbuscarKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtbuscarKeyTyped
        txtbuscar.addKeyListener(new KeyAdapter() {
            @Override
            public void keyReleased(KeyEvent ke) {
                rst.setRowFilter(RowFilter.regexFilter("(?i)" + txtbuscar.getText(), 1));
            }
        });
        rst = new TableRowSorter(tblcons_medicamento.getModel());
        tblcons_medicamento.setRowSorter(rst);
    }//GEN-LAST:event_txtbuscarKeyTyped

    private void btnSeleccionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSeleccionarActionPerformed
        int FilaSelec = tblcons_medicamento.getSelectedRow();
        if (FilaSelec >= 0) {
            String dato[] = new String[2];
            dato[0] = (tblcons_medicamento.getValueAt(FilaSelec, 0).toString());
            dato[1] = (tblcons_medicamento.getValueAt(FilaSelec, 1).toString());

            frmmandet_receta.txtid_medicamento.setText(dato[0]);
            frmmandet_receta.txtnombre_medicamento.setText(dato[1]);
            dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Seleccione algo");
        }
    }//GEN-LAST:event_btnSeleccionarActionPerformed

